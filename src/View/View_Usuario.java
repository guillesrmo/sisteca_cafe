    void habilitar() {
        txt_usuario.setEnabled(true);
        txt_contrasena.setEnabled(true);
        check_estado.setEnabled(true);

    }

    void deshabilitar() {
        txt_usuario.setEnabled(false);
        txt_contrasena.setEnabled(false);
        check_estado.setEnabled(false);
        btnguardar.setEnabled(false);
        btneliminar.setEnabled(false);
        btncancelar.setEnabled(false);
        btn_editar.setEnabled(false);

    }

    void limpiar() {
        txt_usuario.setText("");
        txt_contrasena.setText("");
        check_estado.setSelected(true);
    }

    void cargar_usuario(String cod_usuario) {
        List<Entity_Usuario> usuario = new Controller_Usuario().buscar_cod_usuario(cod_usuario);
        txt_id.setText(usuario.get(0).getUsuario_id());
        txt_usuario.setText(usuario.get(0).getUsuario_user());
        txt_contrasena.setText(usuario.get(0).getUsuario_contrasena());
        check_estado.setSelected(usuario.get(0).getUsuario_estado());
        txt_cod_usuario.setText(usuario.get(0).getUsuario_cod());
        

    }
    private void filtro(String consulta, JTable jtableBuscar) {
        DefaultTableModel dm = (DefaultTableModel) jtableBuscar.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<>(dm);
        jtableBuscar.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(consulta));
    }

