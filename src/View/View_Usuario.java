    private void btnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardarActionPerformed
        // TODO add your handling code here:
        if (txt_id.getText().length() == 0) {
            Function_ShowMessageDialog.ShowMessageDialogvalidacion(rootPane, "CAMPO VACIO");
            return;
        }
        if (txt_cod_usuario.getText().length() == 0) {
            Function_ShowMessageDialog.ShowMessageDialogvalidacion(rootPane, "CAMPO VACIO");
            return;
        }
        if (txt_usuario.getText().length() == 0) {
            Function_ShowMessageDialog.ShowMessageDialogvalidacion(rootPane, "CAMPO VACIO");
            return;
        }
        if (txt_contrasena.getText().length() == 0) {
            Function_ShowMessageDialog.ShowMessageDialogvalidacion(rootPane, "CAMPO VACIO");
            return;
        }
        
        entity_usuario = new Entity_Usuario(txt_id.getText(), txt_cod_usuario.getText(), txt_usuario.getText(), txt_contrasena.getText(), check_estado.isSelected());
        String estado = new Controller_Usuario().Add_Usuario(entity_usuario);
        limpiar();
        deshabilitar();
        txt_cod_usuario.setText("");
        txt_id.setText("");
        txt_cod_usuario.setEnabled(true);
        txt_cod_usuario.requestFocus();
        //controller_usuario.Add_Usuario(entity_usuario);
    }//GEN-LAST:event_btnguardarActionPerformed

