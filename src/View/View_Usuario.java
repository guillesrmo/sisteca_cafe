    private void txt_cod_usuarioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cod_usuarioKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String id_usuario = new Controller_Usuario().Id_Usuario(txt_cod_usuario.getText());
            if (id_usuario.equals("0")) {
                limpiar();
                habilitar();
                cargar_usuario(txt_cod_usuario.getText());
                btnguardar.setEnabled(true);
                btneliminar.setEnabled(true);
                btncancelar.setEnabled(true);
                btn_editar.setEnabled(true);
            } else if (id_usuario.equals("fallo")) {

            } else {
                limpiar();
                habilitar();
                txt_id.setText(id_usuario);
                btnguardar.setEnabled(true);
                btneliminar.setEnabled(true);
                btncancelar.setEnabled(true);
                btn_editar.setEnabled(false);

            }
            txt_cod_usuario.setEnabled(false);
            txt_usuario.requestFocus();
            evt.consume();
        }
    }//GEN-LAST:event_txt_cod_usuarioKeyPressed

