    private void tabla_usaurioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_usaurioMouseClicked
        // TODO add your handling code here:
        punto_operacion.reset_variable_usuario();
        int fila = tabla_usaurio.rowAtPoint(evt.getPoint());
        int column = tabla_usaurio.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tabla_usaurio.getRowHeight();
        if (row < tabla_usaurio.getRowCount() && row >= 0 && column < tabla_usaurio.getColumnCount() && column >= 0) {
            Object value = tabla_usaurio.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton boton = (JButton) value;

                if (boton.getName().equals("btneliminar")) {

                    if (tabla_usaurio.getSelectedRow() != -1) {
                        /*DefaultTableModel model = (DefaultTableModel) tabla_productos_presentacion.getModel();
                        model.removeRow(tabla_productos_presentacion.getSelectedRow());*/
                        if (tabla_usaurio.getValueAt(fila, 1).toString().equals("")) {
                            DefaultTableModel model = (DefaultTableModel) tabla_usaurio.getModel();
                            model.removeRow(tabla_usaurio.getSelectedRow());
                        } else if (tabla_usaurio.getValueAt(fila, 1).toString().equals(null)) {
                            System.out.println("1");
                        } else {
                            //String respuesta=new Controller_Empresa().delete_sucursal(tabla_punto_operacion.getValueAt(tabla_punto_operacion.getSelectedRow(), 1).toString());
                           
                            if (punto_operacion.delete_punto_operacion_usuario(tabla_usaurio.getValueAt(tabla_usaurio.getSelectedRow(), 1).toString()).equals("exito")) {
                                DefaultTableModel model = (DefaultTableModel) tabla_usaurio.getModel();
                                model.removeRow(tabla_usaurio.getSelectedRow());
                            }else{
                                JOptionPane.showMessageDialog(rootPane,"ERROR NO PUEDO ELIMINARSE");
                        
                            }
                        }
                    } else {
                        JOptionPane.showMessageDialog(rootPane, "sss");
                    }

                }

            }
            if (value instanceof JCheckBox) {
                //((JCheckBox)value).doClick();
                JCheckBox ch = (JCheckBox) value;
                if (ch.isSelected() == true) {
                    ch.setSelected(false);
                }
                if (ch.isSelected() == false) {
                    ch.setSelected(true);
                }

            }
        }
    }//GEN-LAST:event_tabla_usaurioMouseClicked

