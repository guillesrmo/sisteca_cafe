    void cargar_usuario(String cod_usuario) {
        List<Entity_Punto_operacion> punto_operacion = new Controller_Punto_operacion().buscar_punto_operacion(cod_usuario);
        txt_id_punto_operacion.setText(punto_operacion.get(0).getPunto_op_id());
        txtcod_punto_operacion.setText(punto_operacion.get(0).getPunto_op_cod());
        txt_id_sucursal.setText(punto_operacion.get(0).getPunto_op_sucursal());
        check_estado.setSelected(punto_operacion.get(0).getPunto_op_estado());
        txt_ruc.setText(punto_operacion.get(0).getEmpresa_ruc());
        txt_razon_social.setText(punto_operacion.get(0).getEmpresa_razon_social());
        txt_razon_comercial.setText(punto_operacion.get(0).getSucursal_cod());
    }
