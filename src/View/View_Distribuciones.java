    private void tabla_guiaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_guiaMouseClicked
        // TODO add your handling code here:

        int fila = tabla_guia.rowAtPoint(evt.getPoint());
        int column = tabla_guia.getColumnModel().getColumnIndexAtX(evt.getX());
        int row = evt.getY() / tabla_guia.getRowHeight();
        if (row < tabla_guia.getRowCount() && row >= 0 && column < tabla_guia.getColumnCount() && column >= 0) {
            Object value = tabla_guia.getValueAt(row, column);
            if (value instanceof JButton) {
                ((JButton) value).doClick();
                JButton boton = (JButton) value;

                if (boton.getName().equals("btneliminar")) {

                    if (tabla_guia.getSelectedRow() != -1) {
                        /*DefaultTableModel model = (DefaultTableModel) tabla_productos_presentacion.getModel();
                        model.removeRow(tabla_productos_presentacion.getSelectedRow());*/

                        if (tabla_guia.getValueAt(fila, 1).toString().length() > 0) {

                            int dialogButton = JOptionPane.YES_NO_OPTION;
                            int dialogResult = JOptionPane.showConfirmDialog(this, "ESTA SEGURO ELIMINAR", "Mensaje", dialogButton);
                            if (dialogResult == 0) {
                                distribucion.delete(tabla_guia.getValueAt(tabla_guia.getSelectedRow(), 1).toString());
                                DefaultTableModel model = (DefaultTableModel) tabla_guia.getModel();
                                model.removeRow(tabla_guia.getSelectedRow());
                            } else {
                                System.out.println("No Option");
                            }
                        }

                    } else {
                        JOptionPane.showMessageDialog(rootPane, "sss");
                    }

                }
                if (boton.getName().equals("btnCAE")) {

                    Map p = new HashMap();
                    p.put("venta", tabla_guia.getValueAt(tabla_guia.getSelectedRow(), 1).toString());
                    JasperReport report;
                    JasperPrint print;

                    try {
                        report = JasperCompileManager.compileReport(new File("").getAbsolutePath()
                                + "/src/Reportes/Jaspersoft_Cardex_Guia.jrxml");
                        print = JasperFillManager.fillReport(report, p, cxn.conectardb());
                        JasperViewer view = new JasperViewer(print, false);
                        view.setTitle("STOCK");
                        view.setVisible(true);
                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                }
                if (boton.getName().equals("btnViewTablaCAE")) {

                    distribucion.tabla_producto(tabla_product, tabla_guia.getValueAt(tabla_guia.getSelectedRow(), 1).toString());
                    Dimension desktopSize = View_Escritorio.desktopPane.getSize();
                    Dimension FrameSize = Jdialog_Producto.getSize();
                    Jdialog_Producto.setLocation((desktopSize.width - FrameSize.width) / 2, (desktopSize.height - FrameSize.height) / 2);
                    Jdialog_Producto.setVisible(true);

                }
                if (boton.getName().equals("btnImprmirCAE")) {

                    Map p = new HashMap();
                    p.put("venta", tabla_guia.getValueAt(tabla_guia.getSelectedRow(), 1).toString());
                    JasperReport report;
                    JasperPrint print;

                    try {
                        report = JasperCompileManager.compileReport(new File("").getAbsolutePath()
                                + "/src/Reportes/Jaspersoft_Cardex_Guia.jrxml");
                        print = JasperFillManager.fillReport(report, p, cxn.conectardb());
                        JasperPrintManager.printReport(print, true);
                        // JasperExportManager.exportReportToPdfFile(print, "factura.pdf");
                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                }
                if (boton.getName().equals("btnPDFCAE")) {

                    Map p = new HashMap();
                    p.put("venta", tabla_guia.getValueAt(tabla_guia.getSelectedRow(), 1).toString());
                    JasperReport report;
                    JasperPrint print;

                    try {
                        report = JasperCompileManager.compileReport(new File("").getAbsolutePath()
                                + "/src/Reportes/Jaspersoft_Cardex_Guia.jrxml");
                        print = JasperFillManager.fillReport(report, p, cxn.conectardb());

                        JFileChooser chooser = new JFileChooser();
                        FileNameExtensionFilter filter = new FileNameExtensionFilter("Archivos de pdf", "pdf");
                        chooser.setFileFilter(filter);
                        chooser.setDialogTitle("Guardar archivo");
                        chooser.setAcceptAllFileFilterUsed(false);
                        if (chooser.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {
                            String ruta = chooser.getSelectedFile().toString().concat(".pdf");
                            JasperExportManager.exportReportToPdfFile(print, ruta);

                        }
                    } catch (Exception e) {
                        e.printStackTrace();
                    }

                }
                if (boton.getName().equals("btnGuiaCE")) {
                    int dialogButton = JOptionPane.YES_NO_OPTION;
                    int dialogResult = JOptionPane.showConfirmDialog(this, "ESTA SEGURO EMITIR GUIA DE REMISION", "Mensaje", dialogButton);
                    if (dialogResult == 0) {
                        List<String> respuesta = new CE_Guia().apiConsume(tabla_guia.getValueAt(tabla_guia.getSelectedRow(), 1).toString());
                        tabla_guia.setValueAt(respuesta.get(3), tabla_guia.getSelectedRow(), 11);
                        distribucion.Add_sucursal(tabla_guia.getValueAt(tabla_guia.getSelectedRow(), 11).toString(), tabla_guia.getValueAt(tabla_guia.getSelectedRow(), 10).toString());
                    } else {
                        System.out.println("No Option");
                    }

                }
                if (boton.getName().equals("btnGuiaPDF")) {
                    openURL(tabla_guia.getValueAt(tabla_guia.getSelectedRow(), 11).toString() + ".pdf");
                }
                if (boton.getName().equals("btnImprmirGIA")) {

                    try {
                        descargar_pdf(tabla_guia.getValueAt(tabla_guia.getSelectedRow(), 11).toString() + ".pdf", "src\\Descargas\\guia_remision.pdf");
                        PDDocument document = PDDocument.load(new File("C:\\Users\\pro\\Downloads\\sss.pdf"));

                        PrinterJob job = PrinterJob.getPrinterJob();

                        //LOGGER.log(Level.INFO, "Mostrando el dialogo de impresion");
                        if (job.printDialog() == true) {
                            job.setPageable(new PDFPageable(document));

                            //   LOGGER.log(Level.INFO, "Imprimiendo documento");
                            job.print();
                        }

                    } catch (IOException ex) {
                        Logger.getLogger(View_Distribuciones.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (PrinterException ex) {
                        Logger.getLogger(View_Distribuciones.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }
                if (value instanceof JCheckBox) {
                    //((JCheckBox)value).doClick();
                    JCheckBox ch = (JCheckBox) value;
                    if (ch.isSelected() == true) {
                        ch.setSelected(false);
                    }
                    if (ch.isSelected() == false) {
                        ch.setSelected(true);
                    }

                }
            }
        }
    }//GEN-LAST:event_tabla_guiaMouseClicked


