    private void tabla_ventaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_ventaMouseClicked
        // TODO add your handling code here:

        if (evt.getClickCount() == 2) {
            String resultado = JOptionPane.showInputDialog("Introduzca DATO");
            if (tabla_venta.columnAtPoint(evt.getPoint()) == 1) {
                for (int i = 0; i < tabla_venta.getRowCount(); i++) {

                    tabla_venta.setValueAt(resultado, i, 1);

                }
            }
            if (tabla_venta.columnAtPoint(evt.getPoint()) == 2) {

                for (int i = 0; i < tabla_venta.getRowCount(); i++) {
                    tabla_venta.setValueAt(resultado, i, 2);

                }
            }
        }


    }//GEN-LAST:event_tabla_ventaMouseClicked

